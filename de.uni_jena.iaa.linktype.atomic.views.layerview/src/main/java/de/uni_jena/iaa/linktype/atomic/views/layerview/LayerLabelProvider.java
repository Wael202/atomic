	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ITableLabelProvider#getColumnText(java.lang.Object, int)
	 */
	@Override
	public String getColumnText(Object element, int columnIndex) {
		SDocumentGraph graph = view.getGraph();
		EList<SLayer> layers = graph.getSLayers();
		switch (columnIndex) {
		case 0:
			EList<SLayer> layer = graph.getSLayerByName((String) element);
			if (layer.isEmpty()) { // I.e. this is manually added "NO ASSIGNED LEVEL"
				return Integer.toString(layers.size()); // Will always be last due to how the list is built
			}
			else {
				return Integer.toString(layers.indexOf(layer.get(0)));
			}
			
		case 1:
			return element.toString();

		default:
			break;
		}
		return null;
	}

